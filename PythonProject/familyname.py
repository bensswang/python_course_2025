def find_unique_name_by_value(value, scope=None):
    scope = globals() if scope is None else scope
    hits = [k for k, v in scope.items() if v == value]
    if len(hits) == 1:
        return hits[0]
    if len(hits) == 0:
        return None
    raise ValueError(f"不唯一，找到多個名字：{hits}")

